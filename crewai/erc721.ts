import { Contract } from './contract';

export class ERC721 extends Contract {
  constructor(address: string, signer?: PhoenixProvider) {
    super(address, ERC721_ABI, signer);
  }

  async name(): Promise<string> {
    return this.functions.name();
  }

  async symbol(): Promise<string> {
    return this.functions.symbol();
  }

  async tokenURI(tokenId: BigNumberish): Promise<string> {
    return this.functions.tokenURI(tokenId);
  }

  async balanceOf(owner: string): Promise<BigNumber> {
    return this.functions.balanceOf(owner);
  }

  async ownerOf(tokenId: BigNumberish): Promise<string> {
    return this.functions.ownerOf(tokenId);
  }

  async getApproved(tokenId: BigNumberish): Promise<string> {
    return this.functions.getApproved(tokenId);
  }

  async isApprovedForAll(owner: string, operator: string): Promise<boolean> {
    return this.functions.isApprovedForAll(owner, operator);
  }

  async transferFrom(
    from: string,
    to: string,
    tokenId: BigNumberish
  ): Promise<TransactionResponse> {
    return this.functions.transferFrom(from, to, tokenId);
  }

  async safeTransferFrom(
    from: string,
    to: string,
    tokenId: BigNumberish
  ): Promise<TransactionResponse> {
    return this.functions['safeTransferFrom(address,address,uint256)'](from, to, tokenId);
  }

  async approve(to: string, tokenId: BigNumberish): Promise<TransactionResponse> {
    return this.functions.approve(to, tokenId);
  }

  async setApprovalForAll(operator: string, approved: boolean): Promise<TransactionResponse> {
    return this.functions.setApprovalForAll(operator, approved);
  }
}

