import { Contract } from './contract';

export class ERC20 extends Contract {
  constructor(address: string, signer?: PhoenixProvider) {
    super(address, ERC20_ABI, signer);
  }

  async name(): Promise<string> {
    return this.functions.name();
  }

  async symbol(): Promise<string> {
    return this.functions.symbol();
  }

  async decimals(): Promise<number> {
    return this.functions.decimals();
  }

  async totalSupply(): Promise<BigNumber> {
    return this.functions.totalSupply();
  }

  async balanceOf(account: string): Promise<BigNumber> {
    return this.functions.balanceOf(account);
  }

  async transfer(to: string, amount: BigNumberish): Promise<TransactionResponse> {
    return this.functions.transfer(to, amount);
  }

  async allowance(owner: string, spender: string): Promise<BigNumber> {
    return this.functions.allowance(owner, spender);
  }

  async approve(spender: string, amount: BigNumberish): Promise<TransactionResponse> {
    return this.functions.approve(spender, amount);
  }

  async transferFrom(
    from: string,
    to: string, 
    amount: BigNumberish
  ): Promise<TransactionResponse> {
    return this.functions.transferFrom(from, to, amount);
  }
}

